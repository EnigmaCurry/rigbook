def approx_grid_for_state(state: str) -> str | None:
    """Return approximate 4-char grid for a US state abbreviation."""
    coords = US_STATE_CENTERS.get(state.upper())
    if coords:
        return latlon_to_grid(coords[0], coords[1])
    return None


def approx_grid_for_country(country_code: str) -> str | None:
    """Return approximate 4-char grid for an ISO country code."""
    coords = COUNTRY_CENTERS.get(country_code.upper())
    if coords:
        return latlon_to_grid(coords[0], coords[1])
    return None
