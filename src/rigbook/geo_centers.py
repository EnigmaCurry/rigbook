"""Approximate geographic centers for US states and countries.

Used to place spots on the map when no Maidenhead grid is available.
"""


def latlon_to_grid(lat: float, lon: float) -> str:
    """Convert lat/lon to a 4-character Maidenhead grid square."""
    lon = lon + 180
    lat = lat + 90
    field_lon = int(lon / 20)
    field_lat = int(lat / 10)
    sq_lon = int((lon % 20) / 2)
    sq_lat = int(lat % 10)
    return (
        chr(field_lon + ord("A"))
        + chr(field_lat + ord("A"))
        + str(sq_lon)
        + str(sq_lat)
    )


